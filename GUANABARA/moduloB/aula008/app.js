/* 
    Operadores de comparação
    
    "=" é uma atribuição
    "==" é uma comparação de valor
    "===" é uma comparação de valor e tipo
*/

5 > 2; // true
5 < 2; // false
5 >= 2; // true
5 <= 2; // false
5 == 2; // false
5 != 2; // true
5 === 2; // false
5 !== 2; // true
5 == '5'; // true
5 === '5'; // false
5 != '5'; // false
5 !== '5'; // true

// Operadores lógicos
// && = E
// || = OU
// ! = NÃO

/* 
    Operador ternário
    (condição) ? valor1 : valor2
    Se a condição for verdadeira, retorna o valor1, caso contrário retorna o valor2
*/

media >= 7.0 ? 'Aprovado' : 'Reprovado'; 
// Se a média for maior ou igual a 7.0, retorna 'Aprovado', caso contrário retorna 'Reprovado'
